
public class DataTest01 {

	public static void main(String[] args) {
		System.out.println(10.5);//double타입 실수값
		System.out.println('a');//모니터 출력장치와 연결됨. print() 메소드는 출력해준다.
		System.out.println(true);//boolean타입으로 참을 뜻한다.
		System.out.println(10L);//Long타입 실수값
		System.out.println(1.5f);//float타입 실수값
		System.out.println("홍길동");//""이중따옴표로 처리된 문자열. 문자열은 기본자료형이 아닌 참조타입.(=레퍼런스 타입 클래스형)
		
		byte a=127;
		a++;
		System.out.println("a="+a);
	}

}
